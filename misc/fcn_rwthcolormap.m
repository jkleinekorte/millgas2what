function RGB = fcn_rwthcolormap(varargin)
% returns n-3-vector with RGB code of 
% official RWTH colors of descending shade in range of 0-1 (not 0-255)
available_colors={...
    'blue'...
    ,'black'...
    ,'magenta'...
    ,'yellow'...
    ,'petrol'...
    ,'turquoise'...
    ,'green'...
    ,'lime'...
    ,'orange'...
    ,'red'...
    ,'bordeaux'...
    ,'purple'...
    };
if isempty(varargin)
    color='blue';
elseif sum(strcmp(varargin{1},{'info','list','help','colors'}))
    for idx=1:length(available_colors)
        disp(available_colors{idx});
    end;  
    color='blue';
else
    color=varargin{1};
end;
switch color
    case 'blue'
        RGB=1/255*[...
            0,84,159 ...
            ;64,127,183 ...
            ;142,186,229 ...
            ;199,211,242 ...
            ;232,241,250 ...
            ];
    case 'black'
        RGB=1/255*[...
            0,0,0 ...
            ;100,101,103 ...
            ;156,158,159 ...
            ;207,209,210 ...
            ;236,237,237 ...
            ];
    case 'magenta'
        RGB=1/255*[...
            227,0,102 ...
            ;233,96,136 ...
            ;241,158,177 ...
            ;249,210,218 ...
            ;253,238,240 ...
            ];
    case 'yellow'
        RGB=1/255*[...
            255,237,0 ...
            ;255,240,85 ...
            ;255,245,155 ...
            ;255,255,209 ...
            ;255,253,238 ...
            ];
    case 'petrol'
        RGB=1/255*[...
            0,97,101 ...
            ;45,127,131 ...
            ;125,164,167 ...
            ;191,208,209 ...
            ;230,236,236 ...
            ];
    case 'turquoise'
        RGB=1/255*[...
            0,152,161 ...
            ;0,177,183 ...
            ;137,204,207 ...
            ;202,231,231 ...
            ;235,246,246 ...
            ];
    case 'green'
        RGB=1/255*[...
            87,171,39 ...
            ;141,192,96 ...
            ;184,214,152 ...
            ;221,235,206 ...
            ;242,247,236 ...
            ];
    case 'lime'
        RGB=1/255*[...
            189,205,0 ...
            ;208,217,92 ...
            ;224,230,154 ...
            ;240,243,208 ...
            ;249,250,237 ...
            ];
    case 'orange'
        RGB=1/255*[...
            246,168,0 ...
            ;250,190,80 ...
            ;253,212,143 ...
            ;254,234,201 ...
            ;255,247,234 ...
            ];
    case 'red'
        RGB=1/255*[...
            204,7,30 ...
            ;216,92,65 ...
            ;230,150,121 ...
            ;243,205,187 ...
            ;250,235,227 ...
            ];
    case 'bordeaux'
        RGB=1/255*[...
            161,16,53 ...
            ;182,82,86 ...
            ;205,139,135 ...
            ;229,197,192 ...
            ;245,232,229 ...
            ];
    case 'purple'
        RGB=1/255*[...
            97,33,88 ...
            ;131,78,117 ...
            ;168,133,158 ...
            ;210,192,205 ...
            ;237,229,234 ...
            ];
    case 'lavendel'
        RGB=1/255*[...
            122,111,172 ...
            ;155,145,193 ...
            ;188,181,215 ...
            ;222,218,235 ...
            ;242,240,247 ...
            ];
end
if nargin==2
    if ismember(varargin{2},[1,2,3,4,5]);
        RGB=RGB(varargin{2},:);
    end
end